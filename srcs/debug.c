#include "fdf.h"
#include <stdio.h>

void					matrix_print(t_matrix m)
{
	int					i;
	int					j;

	i = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
			printf("%*d%c", 5, (int)m[i][j], j != 3 ? ' ' : '\n');
	}
	printf("______________\n");
}
